Linux commands:
    $ cd = change directory
    $ pwd = print working directory
    $ ls = lists directory contents, displaying flies and sub directories
    $ chmod = changes the permissions for files and directories
    $ ll= lists names of the files in the current directory along with permissions, date, time and size
    $ more = view the contents of a text file one screen at a time
    $ less = view the contents of a file one page at a time
    $ cat = concatenate, used to read, display, nd concatenate text files
    $ man = manual

color meaning:
    White: Regular files (Txt)
    Blue: directories
    Green: Executable flies
    Red: Compressed files
    Cyan: symbolic links
    Yellow: Device files
    Magenta: Image files
